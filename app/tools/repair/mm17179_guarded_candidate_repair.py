#!/usr/bin/env python3
"""Guarded MM-17179 candidate repair policy helpers.

This module captures the H12 runtime-validated repair chain for the
MM-17179/ROI4987 fillable-form blocker class. It is intentionally conservative:
it gates adoption on qpdf, render, PDF/UA, WCAG and ISO results, and it preserves
the caveat that generated /Alt labels are validator-safe placeholders until
human reviewed.
"""
from __future__ import annotations

import argparse
import json
import re
from pathlib import Path
from typing import Any, Iterable

TERMINAL_VALIDATED = "AGENT_CANDIDATE_REPAIR_VALIDATED"
TERMINAL_REJECTED = "AGENT_CANDIDATE_REPAIR_CREATED_BUT_REJECTED"
TERMINAL_MISSING_EVIDENCE = "AGENT_CANDIDATE_REPAIR_BLOCKED_BY_MISSING_EVIDENCE"
TERMINAL_VALIDATION_REGRESSION = "AGENT_CANDIDATE_REPAIR_BLOCKED_BY_VALIDATION_REGRESSION"
TERMINAL_UNSAFE_REVERTED = "AGENT_CANDIDATE_REPAIR_UNSAFE_REVERTED"

VALIDATED_SCOPE = [
    "PDF/UA-1/7.21.7",
    "PDF/UA-1/7.21.4.1",
    "PDF/UA-1/7.18.4",
    "PDF/UA-1/7.18.1",
]
TARGET_RULES = tuple(VALIDATED_SCOPE)
PLACEHOLDER_ALT_NOTE = "validator-safe placeholder descriptions derived from existing generic field names"


def write_json(path: Path, payload: dict[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, indent=2, sort_keys=True) + "\n")


def load_json(path: Path) -> dict[str, Any]:
    data = json.loads(path.read_text())
    if not isinstance(data, dict):
        raise ValueError(f"expected JSON object: {path}")
    return data


def pdf_literal_string(text: str) -> str:
    return "(" + text.replace("\\", "\\\\").replace("(", "\\(").replace(")", "\\)") + ")"


def clean_pdf_text(value: Any) -> str | None:
    if value is None:
        return None
    if isinstance(value, str) and value.startswith("u:"):
        return value[2:]
    return str(value)


def qpdf_objects(qpdf_json: dict[str, Any]) -> dict[str, Any]:
    try:
        objects = qpdf_json["qpdf"][1]
    except Exception:
        objects = qpdf_json.get("objects", {})
    if not isinstance(objects, dict):
        raise ValueError("qpdf JSON object dictionary not found")
    return objects


def object_value(objects: dict[str, Any], ref: str | None) -> dict[str, Any]:
    if not ref:
        return {}
    obj = objects.get(f"obj:{ref}", objects.get(ref, {}))
    value = obj.get("value") if isinstance(obj, dict) else None
    return value if isinstance(value, dict) else {}


def rule_counts_from_verapdf_xml(path: Path, clauses: Iterable[str] = TARGET_RULES) -> dict[str, int]:
    text = path.read_text(errors="replace")
    counts: dict[str, int] = {}
    for rule_id in clauses:
        clause = rule_id.split("/")[-1]
        match = re.search(r'<rule[^>]*clause="' + re.escape(clause) + r'"[^>]*failedChecks="([^"]+)"', text)
        counts[rule_id] = int(match.group(1)) if match else 0
    counts["failed_rule_elements_total"] = len(re.findall(r'<rule[^>]*status="failed"', text))
    return counts


def semantic_quality_notes(*, generated_placeholder_alt: bool) -> dict[str, Any]:
    return {
        "form_alt_labels": PLACEHOLDER_ALT_NOTE if generated_placeholder_alt else "not generated by this repair",
        "human_review_required_for_label_quality": bool(generated_placeholder_alt),
        "safe_to_claim_validator_pass": True,
        "safe_to_claim_human_reviewed_field_labels": not generated_placeholder_alt,
        "safe_to_claim_production_ready_without_label_review": not generated_placeholder_alt,
    }


def evaluate_adoption_gates(
    *,
    qpdf_result: dict[str, Any],
    render_compare: dict[str, Any],
    profile_results: dict[str, str],
    final_rule_counts: dict[str, int],
    baseline_rule_counts: dict[str, int] | None = None,
) -> dict[str, Any]:
    failed: list[str] = []
    if qpdf_result.get("result") != "PASS":
        failed.append("qpdf_check")
    if render_compare.get("result") != "PASS" or int(render_compare.get("pages_flagged", 0) or 0) != 0:
        failed.append("render_compare")
    for profile_name in ("PDF/UA-1", "WCAG-2-2-Machine", "ISO-32000-1-Tagged"):
        if profile_results.get(profile_name) != "PASS":
            failed.append(f"verapdf_profile:{profile_name}")
    if any(int(final_rule_counts.get(rule, 0) or 0) != 0 for rule in TARGET_RULES):
        failed.append("target_rules_uncleared")
    if int(final_rule_counts.get("failed_rule_elements_total", 0) or 0) != 0:
        failed.append("remaining_failed_rule_elements")
    if baseline_rule_counts:
        for rule in TARGET_RULES:
            if int(final_rule_counts.get(rule, 0) or 0) > int(baseline_rule_counts.get(rule, 0) or 0):
                failed.append(f"regression:{rule}")
    return {
        "terminal_state": TERMINAL_VALIDATED if not failed else TERMINAL_VALIDATION_REGRESSION,
        "adoption_allowed": not failed,
        "failed_gates": failed,
        "safe_to_claim_pdfua_validator_pass": not failed,
    }


def build_final_report(
    *,
    input_pdf: Path,
    output_pdf: Path,
    candidate_pdf: Path,
    rule_count_progression: dict[str, Any],
    qpdf_result: dict[str, Any],
    render_compare: dict[str, Any],
    profile_results: dict[str, str],
    evidence_artifacts: dict[str, Any],
    generated_placeholder_alt: bool,
    adoption_performed: bool,
) -> dict[str, Any]:
    final_counts = rule_count_progression.get("after_form_alt", {})
    baseline_counts = rule_count_progression.get("baseline", {})
    gate = evaluate_adoption_gates(
        qpdf_result=qpdf_result,
        render_compare=render_compare,
        profile_results=profile_results,
        final_rule_counts=final_counts,
        baseline_rule_counts=baseline_counts,
    )
    notes = semantic_quality_notes(generated_placeholder_alt=generated_placeholder_alt)
    adopted = bool(adoption_performed and gate["adoption_allowed"])
    return {
        "terminal_state": gate["terminal_state"],
        "candidate_chain_created": True,
        "candidate_chain_adopted": adopted,
        "adoption_performed": adopted,
        "input_pdf": str(input_pdf),
        "output_pdf": str(output_pdf),
        "candidate_pdf": str(candidate_pdf),
        "validated_scope": VALIDATED_SCOPE,
        "final_validator_state": {
            "PDF/UA-1": profile_results.get("PDF/UA-1"),
            "WCAG-2-2-Machine": profile_results.get("WCAG-2-2-Machine"),
            "ISO-32000-1-Tagged": profile_results.get("ISO-32000-1-Tagged"),
            "remaining_failed_rules": int(final_counts.get("failed_rule_elements_total", 0) or 0),
        },
        "rule_count_progression": rule_count_progression,
        "evidence_artifacts": evidence_artifacts,
        "preservation": {
            "qpdf_check": qpdf_result.get("result"),
            "render_compare": render_compare.get("result"),
            "render_diff_pct": max((float(p.get("diff_pct", 0) or 0) for p in render_compare.get("page_results", []) or [{}]), default=0.0),
            "pages_flagged": int(render_compare.get("pages_flagged", 0) or 0),
        },
        "semantic_quality_notes": notes,
        "validation_gate": gate,
        "safe_to_claim_pdfua_validator_pass": bool(gate["safe_to_claim_pdfua_validator_pass"]),
        "safe_to_claim_human_reviewed_field_labels": bool(notes["safe_to_claim_human_reviewed_field_labels"]),
        "safe_to_claim_production_ready_without_label_review": bool(notes["safe_to_claim_production_ready_without_label_review"]),
    }


def collect_widget_evidence(objects: dict[str, Any]) -> list[dict[str, Any]]:
    widgets: list[dict[str, Any]] = []
    for key, obj in objects.items():
        if not str(key).startswith("obj:"):
            continue
        ref = str(key).replace("obj:", "")
        value = obj.get("value") if isinstance(obj, dict) else None
        if isinstance(value, dict) and value.get("/Subtype") == "/Widget":
            widgets.append({"ref": ref, "parent": value.get("/Parent"), "rect": value.get("/Rect"), "struct_parent": value.get("/StructParent")})
    return widgets


def precondition_report_from_qpdf(qpdf_json: dict[str, Any], *, max_widgets: int = 1000) -> dict[str, Any]:
    objects = qpdf_objects(qpdf_json)
    widgets = collect_widget_evidence(objects)
    catalog = object_value(objects, "1 0 R")
    struct_root_ref = catalog.get("/StructTreeRoot")
    struct_root = object_value(objects, struct_root_ref)
    parent_tree_ref = struct_root.get("/ParentTree") if isinstance(struct_root, dict) else None
    failed: list[str] = []
    if not struct_root_ref:
        failed.append("StructTreeRoot missing")
    if not parent_tree_ref:
        failed.append("ParentTree missing")
    if not widgets:
        failed.append("widget annotations missing")
    if len(widgets) > max_widgets:
        failed.append("widget evidence truncated")
    return {
        "result": "PASS" if not failed else "FAIL",
        "widgets": widgets[:max_widgets],
        "widget_count": len(widgets),
        "widgets_truncated": len(widgets) > max_widgets,
        "struct_tree_root": struct_root_ref,
        "parent_tree": parent_tree_ref,
        "parent_tree_next_key": struct_root.get("/ParentTreeNextKey") if isinstance(struct_root, dict) else None,
        "failed_preconditions": failed,
    }


def conservative_alt_label(field_type: str | None, root_name: str | None, nearest_name: str | None) -> str:
    root = root_name or "unnamed field"
    nearest = nearest_name or ""
    if field_type == "/Btn":
        label = f"Checkbox field {root}"
        if nearest and nearest != root:
            label += f", option {nearest}"
        return label
    if field_type == "/Tx":
        label = f"Text field {root}"
        if nearest and nearest != root:
            label += f", entry {nearest}"
        return label
    label = f"Form field {root}"
    if nearest and nearest != root:
        label += f", item {nearest}"
    return label


def validate_zapfdingbats_checkbox_stream(stream_text: str, resources: dict[str, Any]) -> bool:
    fonts = resources.get("/Font") if isinstance(resources, dict) else None
    if not isinstance(fonts, dict):
        return False
    has_zadb = any(name == "/ZaDb" or value == "17 0 R" for name, value in fonts.items())
    return bool(has_zadb and "/ZaDb" in stream_text and "(4) Tj" in stream_text)


def vector_checkmark_stream(bbox: list[float]) -> str:
    width = float(bbox[2]) if len(bbox) >= 3 else 8.0
    height = float(bbox[3]) if len(bbox) >= 4 else 8.0
    x1, y1 = width * 0.24, height * 0.48
    x2, y2 = width * 0.42, height * 0.28
    x3, y3 = width * 0.77, height * 0.72
    stroke = max(0.7, min(width, height) * 0.12)
    return f"q\n0 g\n{stroke:.4f} w\n1 J\n1 j\n{x1:.4f} {y1:.4f} m\n{x2:.4f} {y2:.4f} l\n{x3:.4f} {y3:.4f} l\nS\nQ\n"


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description="Guarded MM-17179 H12 candidate repair policy/report helper")
    parser.add_argument("--qpdf-json", default="", help="Optional qpdf JSON to evaluate preconditions")
    parser.add_argument("--report-out", required=True, help="Path to write JSON report")
    parser.add_argument("--input", default="")
    parser.add_argument("--output", default="")
    args = parser.parse_args(argv)
    if args.qpdf_json:
        qpdf_report = precondition_report_from_qpdf(load_json(Path(args.qpdf_json)))
        report = {
            "terminal_state": TERMINAL_MISSING_EVIDENCE if qpdf_report["result"] != "PASS" else "READY_FOR_GUARDED_CANDIDATE_REPAIR",
            "input_pdf": args.input or None,
            "output_pdf": args.output or None,
            "preconditions": qpdf_report,
            "adoption_performed": False,
            "safe_to_claim_pdfua_validator_pass": False,
            "safe_to_claim_human_reviewed_field_labels": False,
            "safe_to_claim_production_ready_without_label_review": False,
        }
    else:
        report = {
            "terminal_state": TERMINAL_MISSING_EVIDENCE,
            "reason": "qpdf evidence not supplied",
            "adoption_performed": False,
            "safe_to_claim_pdfua_validator_pass": False,
            "safe_to_claim_human_reviewed_field_labels": False,
            "safe_to_claim_production_ready_without_label_review": False,
        }
    write_json(Path(args.report_out), report)
    print(json.dumps(report, indent=2, sort_keys=True))
    return 0 if report["terminal_state"] != TERMINAL_MISSING_EVIDENCE else 1


if __name__ == "__main__":
    raise SystemExit(main())
